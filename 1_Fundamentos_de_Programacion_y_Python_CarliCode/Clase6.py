# Variables

a = 10
b = 3

# Suma

print ("Suma:", a + b)

resultado_suma = a+b
print ("Suma:", resultado_suma)

# Resta

print ("Resta:", a - b)

resultado_resta = a-b
print ("Resta:", resultado_resta)

# Multiplicacion

print ("Multiplicacion", a * b)

# Potenciacion (Nos permite elevar un numero a la potencia de otro)

print ("Potenciacion", a ** b)

# Division

print ("Division", a / b)

# Division entera (Nos da el resultado de la division sin el resto)

print ("Parte entera de la Division", a // b)

# Modulo (Nos permite obtener el residuo de una division)

print ("Modulo", a % b)


# Asignacion combinada

a /= 2
print(a)

a += 2
print(a)

# Orden de operaciones (PEMDAS)

operation_1 = 2 + 3 * 4
operation_2 = (2 + 3) * 4
print(operation_1)
print(operation_2)

operation_3 = (2+3) * (4**2)/ 8 - 1
print(operation_3)

